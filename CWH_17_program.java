package com.CWH;

public class CWH_17_program {

    public static void main(String[]args){

        //conditional statement - SWITCH

        int day = 2;

        switch(day) {
            case 1 :
                System.out.println("monday");
                break;
            case 2 :
                System.out.println("tuesday");
            break;
            default :
                System.out.println("wed-sun");
        }
    }
}
